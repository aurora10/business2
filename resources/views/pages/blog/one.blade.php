<h2>The Process of Developing an MVP</h2>

<h3>Set Your Priorities</h3>

<p>Prioritization is probably the best skill you can learn while launching your own product, and especially an MVP. Prioritize UI and customer support over architecture and mobile apps over their web counterparts. You need to start building reputation immediately and attract clients with a clean, functional UI &ndash; you will have time to contemplate architecture and additional platforms later down the line.</p>

<blockquote>
<p>Creating an MVP can be a good way to get a maximum amount of validated learning about customers with the least effort. However, in my opinion some startups fail to deliver a MVP with an actual experience built in. It tends to become too tech and too little product, and the risk is that you actually don&rsquo;t get to test your idea well enough. That is why, we at JumpStory talk about building MLP&rsquo;s instead &ndash; short for Minimum Loveable Product.</p>
</blockquote>

<p style="text-align: right;"><strong>Jonathan Low, CEO at JumpStory</strong></p>

<h3>Where To Save money</h3>

<p>There are a number of decisions you can make that will help your budget. Leave out the web version for now: the majority of your users will come from mobile anyway. Opt for cross-platform app development instead of native: this will help you update frequently, change things on the fly and have all code in one place. You can also choose monolith architecture that is not as elaborate as microservices, and evolve it later.</p>

<h3>Choose Your Tech Wisely</h3>

<p>Not all the technologies for software development are made equal. You already know that cross-platform tech is usually cheaper than full on native development, provided you do not have some special features involving camera or geolocation.</p>

<h4>Mobile</h4>

<p>Since cross-platform development is usually a good choice for a constrained budget, you should opt for React Native or Flutter. For OS specific features or OS specific UI design and layout a platform-based approach would work best. But in this case it would be better to develop for one platfrom at a time. This would save bugdet on back and forth changes which most probably would take place initially. However, this approach takes more time compared to development of an app for both platforms at the same time.</p>

<h4>Web</h4>

<p>React and Angular are the best options, both being the gold standard in web development in 2020.</p>

<h4>Backend</h4>

<p>With backend, there are a lot of options and things to consider:</p>

<ul>
	<li><strong>Node.js</strong> &ndash; based on JavaScript, which guarantees you a quick start and quick results and additionally save up some money along the way</li>
	<li>.<strong>NET Core</strong> &ndash; a mature platform and a powerful versatile instrument in professional hands with a steep learning curve</li>
</ul>

<h4>Hosting</h4>

<p>If you have an option of setting up your own servers to host your application, you can go for it but do keep in mind that this entails additional expenses on DevOps operations since you will have to set it all up manually and from scratch. The other option would be cloud hosting where you can rent a cloud environment and services for your project and supply them with a variety of additional services needed for the micro-service architecture such as AWS Lambda. Database choices are heavily project-based and will depend on the type of application you are building: Postgress, Mongo, MS SQL, Amazon RDS, Amazon DynamoDB, Azure SQL, etc.</p>

<ul>
	<li><strong>On-premise</strong>: if you have your own servers, you can use Kubernetes and Docker. This will save you money in the short run, but the upkeep will cost more in the long run.</li>
	<li><strong>AWS, Azure, Google Cloud</strong> &ndash; if you don&rsquo;t have your own infrastructure, cloud hosting is a cheaper and more reasonable option. As an added bonus, you have to have much less DevOps expenses at the start of operation.</li>
</ul>

