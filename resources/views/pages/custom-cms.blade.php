			<div class="item">
	<h4 class="bold">Web Content Management System (WCMS)</h4>
	<div class="textbox"><p>It is the most common variety of content management systems. As the name suggests, WCMSs manage the content of websites. They are usually very flexible and can be equally suitable for blogs, news aggregators, online galleries, ecommerce marketplaces, and other site types. Some of them provide a necessary set of features out of the box, while other WCMSs need third-party plugins and services.</p>
</div>
</div>

<div class="item">
	<h4 class="bold">Enterprise Content Management System (ECMS)</h4>
	<div class="textbox"><p>This is a content management system that has extended functionality and scale. It must be adapted to the needs and peculiarities of corporate use: business automation, analysis, etc. Usually, document management systems (DMS) are also included in this category.</p>
</div>
</div>

<div class="item">
	<h4 class="bold">Component Content Management System (CCMS)</h4>
	<div class="textbox"><p> It manages structured content based on a different approach that handles content as components rather than documents. Various objects may be regarded as components: paragraphs, definitions, single words. One key feature is that a component is saved in the system only once and then reused via links rather than duplicated. Another characteristic feature is that all components are provided with metadata that facilitates searching, version tracking, and other similar activities.</p>
</div>
</div>

<div class="item">
	<h4 class="bold">Digital Asset Management System (DAMS)</h4>
	<div class="textbox"><p> It functions as a library for digital content, for example, books, audio, video, photos, and other creative works. Usually, DAMS also provides media content publishing and distribution functionalities.</p>
</div>
</div>

		
