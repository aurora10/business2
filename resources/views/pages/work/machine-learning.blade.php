						<h3>Projects</h3>
						<div class="textbox">
							<p>All models trained using the platform exist in the form of projects - a collection of all relevant data, allowing for a quick overview of all user projects and their status.</p>

<p><img alt="" src="../files/content/casestudiesimg/first-step-ai/microsoftteams-image-(34).png" /></p>

						</div>
						
						<h3>Dataset</h3>
						<div class="textbox">
							<p>The platform allows users to add and annotate their own datasets by uploading images and marking them up. Before the markup starts, users can create labels that represent the objects which need to be detected to use for a markup later.</p>

<p><img alt="" src="../files/content/casestudiesimg/first-step-ai/microsoftteams-image-(35).png"  /></p>

<p>After the images are ready, the platform performs dataset analysis and provides relevant statistical information, like total image count, along with a balancing score that shows how good the dataset is in terms of objects distribution by classes. This makes it possible for the user to create a well-balanced data search for efficient machine learning model training. The more balanced the dataset is, the higher the mAP (mean average precision).</p>

<p><img alt="" src="../files/content/casestudiesimg/first-step-ai/microsoftteams-image-(36).png" /></p>

						</div>
						
						<h3>Image Recognition Model Training</h3>
						<div class="textbox">
							<p>After the dataset is complete, users can start training their own machine learning model. They can choose Tensorflow or PyTorch with various sets of base models and input sizes. For model training, we have used Amazon Sagemaker which allows for fast training and significant savings for the client as they only pay for effective training time. During the training process users are able to get real-time information on how their model is improving its detection accuracy.</p>
<p>After the model has been trained, users can check on the final mAp and decide on whether it is high enough or if they want to improve it. When the latter is in case, users can go back to the dataset and add more images or increase its balancing score.</p>

						</div>
						
						<h3>Model Testing And Download</h3>
						<div class="textbox">
							<p>If the users are happy with the mAp, they can test their models within our platform by uploading images and videos not included in the training dataset and seeing how well the model is able to detect the objects. Once the testing is complete, users can download the model in several formats depending on how it will be used:</p>

<ul>
	<li>APK - a ready-made Android mobile app with a user-trained model built-in</li>
	<li>APK source code - ideal for mobile developers and integration into mobile apps</li>
	<li>Various formats to install into different environments (servers, mobile phones, IoT or other edge devices) - TFLite (Quantized/Non-Quantized, Int8/Float16/Float32, Saved Model (.pb)) or YOLOv5 (.pt)</li>
</ul>

<p><img alt="" src="../files/content/casestudiesimg/first-step-ai/microsoftteams-image-(37).png"  /></p>

						</div>
						
						<h3>Architecture And Tech Stack</h3>
						<div class="textbox">
							<p>The platform takes advantage of various Amazon services both for model training itself and the platform's own architecture. We have used AWS Amplify to implement serverless architecture which allows for fast development times as it eliminates the need for DevOps, saving client time and resources. Amazon Sagemaker is used for model training as it simplifies the need for dedicated environment orchestration.</p>
<p>We have implemented a second-tier backend using Python to accommodate Sagemaker and any features the growing startup might need in the future.
</p>
						</div>
						
						<h3>Results</h3>
						<div class="textbox">
							<p>The platform is a simple to use and elegant solution to a high barrier of entry into machine learning development. While it is simple enough for the average user, it is also an invaluable tool for software development teams that need to implement machine learning into their projects but are not ready to hire machine learning developers.</p>

<p>The platform is fully operational and is now in the soft launch stage. We continue system support and plan on introducing improvements and new features in the near future.</p>

