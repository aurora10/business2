						<h3>Case Study</h3>
						<div class="textbox">
							<p>We were tasked with the development of an Android-based banking app for a local bank which would replace an already existing outdated app. The new version had to include the following functionality:</p>

<ol>
	<li>Log in and authorisation</li>
	<li>Bank account status and account management</li>
	<li>Manual and automatic money transfers</li>
	<li>Transaction history</li>
</ol>

<p>One of the primary goals was to not only develop an app which would update the design and improve user experience, but to ensure a high level of security as online banking often falls prey to hackers and malicious users. High security measures are vital to ensure the safety of user accounts and maintain the bank&rsquo;s reputation as a safe and reliable establishment to store funds. We had to work hand in hand with the bank&#39;s security teams to develop the product that would both be safe and convenient to use.</p>

						</div>
						
						<h3>Tech and Approach</h3>
						<div class="textbox">
							<p>The sensitive and complex nature of banking operations meant we needed to collaborate with the bank&#39;s in-house developers and security departments to bring the app to life. High standards of security surrounding all banking data meant we had limited access to data needed for app development. We had to stay in close touch with the bank&rsquo;s developers and security teams and maintain full transparency of the development process to gain access to the needed data. Our development teams had to work around restrictions and safety procedures while staying within time constraints and client&rsquo;s budget.</p>

<p>We used Kotlin and Coroutines framework to develop the app. The backend was created by the bank&rsquo;s in-house developers using Protocol Buffers. We had access to data structures which could be used to generate classes. This approach both reduces development time by eliminating the need to create object types and lowers the chance of query errors as classes are created automatically.</p>

<p>We have implemented multi module app architecture which provides great benefits during the development process. Each module exists separately and can be implemented and tested individually. On top of that this approach makes the reuse of code easy and convenient.</p>

						</div>
						
						<h3>Data Safety and Ecryption</h3>
						<div class="textbox">
							<p>In-house developers had trouble implementing data encryption and, as a consequence, ensuring user data safety that would mirror those used by the bank.</p>

<p>To ensure the safety of user data transfer, we have implemented end-to-end encryption to prevent data spoofing by malicious users through the replacement of a beneficiary account during money transfer. We used a androidx.crypto library to implement data encryption as an out-of-the-box solution was the most cost efficient option for the client.</p>

<p>After the app was completed, it was subjected to a final review from the bank&rsquo;s security department to check once again that the app was up to the latest data security standards.</p>

						</div>
						
						<h3>Results</h3>
						<div class="textbox">
							<p>The resulting app has replaced the outdated version, greatly improving user experience. Bank&rsquo;s clients now have convenient access to their bank accounts and transaction history and have full control of their funds at their fingertips.</p>

<p>We have gained invaluable experience of working around various security procedures and protocols while staying within time and budget limits.</p>

