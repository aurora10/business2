						<h3>Case Study</h3>
						<div class="textbox">
							<p>&nbsp;</p>

<p>The app works by aggregating news from major online news websites, excluding ads, and augmenting them with tags and keywords, and linking them to other news articles from different sources and showing how the subject matter evolved over time.</p>

<p>The news feed is highly customisable and allows the user to choose which websites are used as news sources, as well as set the following preferences:</p>

<ul>
	<li>World, country, or local news</li>
	<li>News themes, like health and fitness, politics, travelling and many more</li>
	<li>Keywords and tags</li>
</ul>

<p>The user can save and bookmark news articles they have found interesting or important. The app also supports offline access to the news feed for when access to the internet is limited.</p>

<p style="text-align: center;"><img alt="" src="../files/content/casestudiesimg/life%20lines/cs-life-lines-i1-min.png" /></p>

						</div>

						<h3>News History</h3>
						<div class="textbox">
							<p>One of the key features is news history. Each news article contains information on how the subject matter evolved over time across different news articles. When the user opens a news article, the app provides links to all related news pieces in chronological order so the user can see the full history of the subject matter over time.</p>

<p>Another important feature is the &ldquo;Other sources&rdquo; section present under every news article. The app automatically shows news articles on the same topic from different websites, so the user can get a well-rounded look at the subject matter.</p>

<p style="text-align: center;"><img alt="" src="../files/content/casestudiesimg/life%20lines/cs-life-lines-i2-min.png" /></p>

						</div>

						<h3>UI and UX</h3>
						<div class="textbox">
							<p>As this is a consumer app, we had to focus on UI and UX to make the app pleasant to use. The news feed loads gradually so the user doesn&#39;t have to wait for the entirety of the content to load. We avoid blank loading screens by showing the information available from the feed when the user clicks on a news article.</p>

<p>The app is now available in AppStore and will soon be available for Android.</p>

